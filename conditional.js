//if-else
var light="white"

if(light=="green"){
    console.log("Go!")
}
else if(light=="yellow"){
    console.log("wait")
}
else{
    console.log("invalid light colour")
}
console.log("outside if")
//switch
var signal="white"
switch(signal){
    case "green":
    console.log("go!");
    break;

    case "yellow":
    console.log("wait");
    break;

    case "red":
    console.log("stop!")
    break;

    default:
    console.log("outside if")
    



}
